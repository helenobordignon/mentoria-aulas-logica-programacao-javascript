// Definir uma função para calcular a média de dois números
export function calcularMedia(n1, n2) {
    return (n1 + n2) / 2;
}

export function somarDoisNumeros(n1, n2) {
    return n1 + n2;
}

export function subtrairDoisNumeros(n1, n2) {
    return n1 - n2;
}

export function multiplicarDoisNumeros(n1, n2) {
    return n1 * n2;
}